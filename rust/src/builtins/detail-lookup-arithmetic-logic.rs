pub(crate) fn lookup_detail_arithmetic_logic(name: &str) -> Option<String> {
    let result = match name {
        "+" => r#"# + - 加算

## 機能
Fold: 2つのベクタを要素ごとに加算します。片方が単一要素ならブロードキャストされます。Stackモードではスタックから指定個数の要素を取り、左から順に加算します。

## 使用法
[ a... ] [ b... ] +
a... [ 個数 ] .. +

## 使用例
[ 1 2 3 ] [ 4 5 6 ] +          # → [ 5 7 9 ]
[ 1 2 3 ] [ 10 ] +             # → [ 11 12 13 ]（ブロードキャスト）

[ 1 ] [ 2 ] [ 3 ] [ 3 ] .. +   # → [ 6 ]（1+2+3）

## 注意
- ベクタ長が異なり、かつブロードキャストできない場合はエラー
- 演算結果が入力と同じ（例: [0] を加算）ならエラー（「変化なしはエラー」原則）"#,

        "-" => r#"# - - 減算

## 機能
Fold: 2つのベクタを要素ごとに減算します。片方が単一要素ならブロードキャストされます。Stackモードではスタックから指定個数の要素を取り、左から順に減算します。

## 使用法
[ a... ] [ b... ] -
a... [ 個数 ] .. -

## 使用例
[ 5 7 9 ] [ 1 2 3 ] -          # → [ 4 5 6 ]
[ 10 20 30 ] [ 5 ] -           # → [ 5 15 25 ]

[ 10 ] [ 3 ] [ 2 ] [ 3 ] .. -  # → [ 5 ]（10-3-2）

## 注意
- ベクタ長が異なり、かつブロードキャストできない場合はエラー
- 演算結果が入力と同じ（例: [0] を減算）ならエラー"#,

        "*" => r#"# * - 乗算

## 機能
Fold: 2つのベクタを要素ごとに乗算します。片方が単一要素ならブロードキャストされます。Stackモードではスタックから指定個数の要素を取り、左から順に乗算します。

## 使用法
[ a... ] [ b... ] *
a... [ 個数 ] .. *

## 使用例
[ 1 2 3 ] [ 4 5 6 ] *          # → [ 4 10 18 ]
[ 1 2 3 ] [ 10 ] *             # → [ 10 20 30 ]

[ 2 ] [ 3 ] [ 4 ] [ 3 ] .. *   # → [ 24 ]（2*3*4）

## 注意
- ベクタ長が異なり、かつブロードキャストできない場合はエラー
- 演算結果が入力と同じ（例: [1] を乗算）ならエラー"#,

        "/" => r#"# / - 除算

## 機能
Fold: 2つのベクタを要素ごとに除算します。結果は分数として厳密に保持されます。片方が単一要素ならブロードキャストされます。Stackモードではスタックから指定個数の要素を取り、左から順に除算します。

## 使用法
[ a... ] [ b... ] /
a... [ 個数 ] .. /

## 使用例
[ 10 20 30 ] [ 2 4 5 ] /       # → [ 5 5 6 ]
[ 10 20 30 ] [ 10 ] /          # → [ 1 2 3 ]

[ 100 ] [ 2 ] [ 5 ] [ 3 ] .. /  # → [ 10 ]（100/2/5）

## 注意
- 0 での除算はエラー
- ベクタ長が異なり、かつブロードキャストできない場合はエラー
- 演算結果が入力と同じ（例: [1] で除算）ならエラー"#,

        "=" => r#"# = - 等価比較

## 機能
Fold: 2つの値が完全に等しいかを判定します。あらゆる型（Scalar, String, Boolean, Vector, NIL）を比較可能です。Stackモードでは指定個数の要素が全て等しいかを判定します。

## 使用法
[ a ] [ b ] =
a... [ 個数 ] .. =

## 使用例
[ 3 ] [ 3 ] =              # → [ TRUE ]
[ 3 ] [ 5 ] =              # → [ FALSE ]
[ 'hi' ] [ 'hi' ] =        # → [ TRUE ]
[ 1 2 ] [ 1 2 ] =          # → [ TRUE ]（ベクタ全体で比較）

[ 3 ] [ 3 ] [ 3 ] [ 3 ] .. =    # → [ TRUE ]

## 注意
- ベクタ全体を1つの値として比較します（要素ごとではない）"#,

        "<" => r#"# < - 小なり比較

## 機能
Fold: 2つのベクタを要素ごとに「左 < 右」で比較します。片方が単一要素ならブロードキャストされます。

## 使用法
[ a... ] [ b... ] <

## 使用例
[ 1 2 3 ] [ 2 2 2 ] <          # → [ TRUE FALSE FALSE ]
[ 1 2 3 ] [ 3 ] <              # → [ TRUE TRUE FALSE ]

## 注意
- `>` は廃止されました。`[ b ] [ a ] <` または `[ a ] [ b ] < NOT` で代替します
- ベクタ長が異なり、かつブロードキャストできない場合はエラー"#,

        "<=" => r#"# <= - 小なりイコール比較

## 機能
Fold: 2つのベクタを要素ごとに「左 <= 右」で比較します。片方が単一要素ならブロードキャストされます。

## 使用法
[ a... ] [ b... ] <=

## 使用例
[ 1 2 3 ] [ 2 2 2 ] <=         # → [ TRUE TRUE FALSE ]
[ 1 2 3 ] [ 3 ] <=             # → [ TRUE TRUE TRUE ]

## 注意
- `>=` は廃止されました。`[ b ] [ a ] <=` または `[ a ] [ b ] <= NOT` で代替します
- ベクタ長が異なり、かつブロードキャストできない場合はエラー"#,

        ">" => r#"# > - 廃止された比較演算子

## 機能
この演算子は廃止されました。`<` を使って代替してください。

## 代替方法
# 旧: [ 5 ] [ 3 ] >
# 新: [ 3 ] [ 5 ] <       （オペランドを逆に）
# 新: [ 5 ] [ 3 ] < NOT   （否定で代替）

## 注意
- 「`<` と `<=` のみを使う」方針により、数直線との対応が明確になります"#,

        ">=" => r#"# >= - 廃止された比較演算子

## 機能
この演算子は廃止されました。`<=` を使って代替してください。

## 代替方法
# 旧: [ 5 ] [ 3 ] >=
# 新: [ 3 ] [ 5 ] <=       （オペランドを逆に）
# 新: [ 5 ] [ 3 ] <= NOT   （否定で代替）

## 注意
- 「`<` と `<=` のみを使う」方針により、数直線との対応が明確になります"#,

        "AND" => r#"# AND - 論理積

## 機能
Fold: 2つのベクタを要素ごとに論理積（AND, Kleene三値）で計算します。片方が単一要素ならブロードキャストされます。`&` は AND の後置エイリアスです。

## 使用法
[ a... ] [ b... ] AND
[ a... ] [ b... ] &

## 使用例
[ TRUE TRUE FALSE FALSE ] [ TRUE FALSE TRUE FALSE ] AND
# → [ TRUE FALSE FALSE FALSE ]

[ TRUE FALSE TRUE ] [ TRUE ] AND
# → [ TRUE FALSE TRUE ]（ブロードキャスト）

## 注意
- 要素は Boolean 型である必要があります
- ベクタ長が異なり、かつブロードキャストできない場合はエラー"#,

        "OR" => r#"# OR - 論理和

## 機能
Fold: 2つのベクタを要素ごとに論理和（OR, Kleene三値）で計算します。片方が単一要素ならブロードキャストされます。

## 使用法
[ a... ] [ b... ] OR

## 使用例
[ TRUE TRUE FALSE FALSE ] [ TRUE FALSE TRUE FALSE ] OR
# → [ TRUE TRUE TRUE FALSE ]

[ TRUE FALSE TRUE ] [ FALSE ] OR
# → [ TRUE FALSE TRUE ]（ブロードキャスト）

## 注意
- 要素は Boolean 型である必要があります
- ベクタ長が異なり、かつブロードキャストできない場合はエラー"#,

        "NOT" => r#"# NOT - 論理否定

## 機能
Map: ベクタの各要素の論理否定を返します。

## 使用法
[ a... ] NOT

## 使用例
[ TRUE FALSE TRUE ] NOT    # → [ FALSE TRUE FALSE ]
[ FALSE ] NOT              # → [ TRUE ]

## 注意
- 要素は Boolean 型である必要があります"#,

        _ => return None,
    };
    Some(result.to_string())
}
