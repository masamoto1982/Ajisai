pub(crate) fn lookup_detail_modifier(name: &str) -> Option<String> {
    let result = match name {
        "TOP" => {
            r#"# . - スタックトップ指定

## 機能
操作対象をスタックトップ（最上位のベクタ）に設定します。デフォルトモードなので、通常は省略できます。

## 使用法
. WORD
→ 次のワードをスタックトップに対して実行

## 使用例
[ 1 2 3 ] . LENGTH     # → [ 1 2 3 ] [ 3 ]
[ 1 2 3 ] LENGTH       # 同じ結果（デフォルト）

## 注意
- ワード実行後、自動的にこのモードにリセットされます"#
        }

        "STAK" => {
            r#"# .. - スタック全体指定

## 機能
操作対象をスタック全体に設定します。対応するワードに対してスタック全体を1つの列として扱います。

## 使用法
.. WORD
→ 次のワードをスタック全体に対して実行

## 使用例
a b c [ 1 ] .. GET     # → a b c [ b ]
1 2 3 4 5 .. LENGTH    # → 1 2 3 4 5 [ 5 ]
a b c .. REVERSE       # → c b a

## 注意
- 対応ワード: GET, INSERT, REPLACE, REMOVE, LENGTH, TAKE, SPLIT, CONCAT, REVERSE, +, -, *, /, MAP など
- ワード実行後、自動的にスタックトップモード (.) にリセットされます"#
        }

        "EAT" => {
            r#"# , - 消費モード指定

## 機能
消費モードを「消費する」（デフォルト）に設定します。オペランドをスタックから取り出して処理します。

## 使用法
, WORD
→ 次のワードでオペランドを消費する

## 使用例
[ 1 ] [ 2 ] , +     # → [ 3 ]（[1]と[2]は消費される）
[ 1 ] [ 2 ] +       # 同じ結果（デフォルト）

## 注意
- デフォルトモードなので、通常は省略できます
- ワード実行後、自動的にこのモードにリセットされます
- 修飾子は順序非依存: `,, ..` と `.. ,,` は同じ動作"#
        }

        "KEEP" => {
            r#"# ,, - 保持モード指定

## 機能
消費モードを「保持する」に設定します。オペランドをスタックに残したまま、結果を末尾に追加します。

## 使用法
,, WORD
→ 次のワードでオペランドを保持し、結果を追加

## 使用例
[ 1 ] [ 2 ] ,, +    # → [ 1 ] [ 2 ] [ 3 ]
[ 3 ] [ 4 ] ,, *    # → [ 3 ] [ 4 ] [ 12 ]

[ 1 ] [ 2 ] [ 3 ] [ 3 ] .. ,, +
# → [ 1 ] [ 2 ] [ 3 ] [ 6 ]  (スタック全体を保持モードで加算)

## 注意
- ワード実行後、自動的に消費モード (,) にリセットされます
- 演算の連鎖に便利です"#
        }

        "SAFE" => {
            r#"# ~ - セーフモード指定

## 機能
セーフモードを有効にします。エラーが発生した場合、エラーを伝播せず NIL を返します。

## 使用法
~ WORD
→ 次のワードでエラーが起きた時に NIL を返す

## 使用例
[ 1 2 3 ] [ 10 ] ~ GET    # → NIL（範囲外アクセスが NIL になる）
[ 1 2 3 ] [ 0 ] ~ GET     # → [ 1 ]（正常時は通常と同じ）

## 注意
- 例外を値で扱いたい場合に使います
- ワード実行後、自動的にリセットされます
- `=>` と組み合わせると NIL のフォールバックを簡潔に書けます"#
        }

        "PIPE" => {
            r#"# PIPE - パイプラインマーカー (糖衣構文: ==)

## 機能
データフローを視覚的に明示するためのマーカーで、実行時には何もしません（no-op）。複数行のコードを読みやすくするために使います。

## 使用法
value == operation

## 使用例
[ 1 2 3 ] == { [ 2 ] * } MAP         # 単一行

[ 1 2 3 4 5 ]
  == { [ 2 ] * } MAP                 # 各要素を2倍
  == { [ 5 ] < NOT } FILTER          # 5以上の要素を抽出
  == [ 0 ] { + } FOLD                # 合計
# → [ 24 ]

## 注意
- 省略しても結果は同じです
- 純粋に視覚的なマーカーで動作に影響しません"#
        }

        "OR-NIL" => {
            r#"# OR-NIL - Nil 合体演算子 (糖衣構文: =>)

## 機能
値が NIL の場合に右側の代替値を返します。他言語の `??` や `?:` に相当します。

## 使用法
value => default
→ value が NIL なら default、そうでなければ value

## 使用例
NIL => [ 0 ]                    # → [ 0 ]
[ 42 ] => [ 0 ]                 # → [ 42 ]

[ 1 2 3 ] [ 10 ] GET => [ 0 ]   # → [ 0 ]（範囲外の保険）
[ 1 2 3 ] [ 0 ] GET => [ 0 ]    # → [ 1 ]

value => fallback1 => fallback2   # 左結合で連鎖可能

## 注意
- NIL のみを置換します（FALSE は置換しません）
- `~` と組み合わせるとエラー時のフォールバックに使えます"#
        }

        "FORC" => {
            r#"# ! - Force フラグ

## 機能
DEF や DEL において依存関係のある他のユーザーワードを含めて強制的に上書き／削除します。

## 使用法
! 'NAME' DEL
→ 依存ワードも含めて削除
! { 定義 } 'NAME' DEF
→ 既存ワード（依存関係あり）を強制的に再定義

## 使用例
! 'DOUBLE' DEL         # DOUBLE を参照する他ワードもまとめて削除

## 注意
- 不用意に使うと依存ワードごと失われるので注意してください
- 単体の DEL/DEF でも依存関係が無ければ成功します"#
        }

        _ => return None,
    };
    Some(result.to_string())
}
