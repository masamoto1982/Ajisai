// Detailed documentation for modifier words (. .. , ,,)

/// Returns detailed documentation for modifier words.
pub(crate) fn lookup_detail_modifier(name: &str) -> Option<String> {
    let result = match name {
        // ============================================================================
        // 操作対象指定
        // ============================================================================

        "." => r#"# . - スタックトップ指定

## 機能
操作対象をスタックトップ（最上位のベクタ）に設定します。
デフォルトの動作モードなので、通常は明示的な指定は不要です。

## 使用法
. WORD
→ 次のワードをスタックトップに対して実行

## 使用例
[ 1 2 3 ] . LENGTH     # → [ 1 2 3 ] [ 3 ]
[ 1 2 3 ] LENGTH       # 同じ結果（デフォルト）

## 注意
- これはデフォルトモードなので、通常は省略可能です
- ワード実行後、自動的にこのモードにリセットされます"#,

        ".." => r#"# .. - スタック全体指定

## 機能
操作対象をスタック全体に設定します。
多くの組み込みワードがスタックモードに対応しています。

## 使用法
.. WORD
→ 次のワードをスタック全体に対して実行

## 使用例
a b c [ 1 ] .. GET      # → a b c [ b ]
（スタック全体から1番目を取得）

1 2 3 4 5 .. LENGTH     # → 1 2 3 4 5 [ 5 ]
（スタックの要素数を取得）

a b c .. REVERSE        # → c b a
（スタック全体を反転）

## 対応ワード
GET, INSERT, REPLACE, REMOVE, LENGTH, TAKE, SPLIT,
CONCAT, REVERSE, LEVEL, +, -, *, /, MAP など

## 注意
- ワード実行後、自動的にスタックトップモード(.)にリセットされます"#,

        // ============================================================================
        // 消費モード指定
        // ============================================================================

        "," => r#"# , - 消費モード指定

## 機能
消費モードを「消費する」（デフォルト）に設定します。
オペランドをスタックから取り出して処理します。

## 使用法
, WORD
→ 次のワードでオペランドを消費する（デフォルト動作）

## 使用例
[ 1 ] [ 2 ] , +      # → [ 3 ]（[1]と[2]は消費される）
[ 1 ] [ 2 ] +        # 同じ結果（デフォルト）

## 注意
- これはデフォルトモードなので、通常は省略可能です
- ワード実行後、自動的にこのモードにリセットされます
- 修飾子は順序非依存: ",, .." と ".. ,," は同じ動作"#,

        ",," => r#"# ,, - 保持モード指定

## 機能
消費モードを「保持する」に設定します。
オペランドをスタックに残したまま処理を行います。

## 使用法
,, WORD
→ 次のワードでオペランドを保持し、結果を追加

## 使用例
[ 1 ] [ 2 ] ,, +     # → [ 1 ] [ 2 ] [ 3 ]
（元の値を残して結果を追加）

[ 3 ] [ 4 ] ,, *     # → [ 3 ] [ 4 ] [ 12 ]
（元の値を残して乗算結果を追加）

## 対応ワード
+, -, *, / などの算術演算

## 修飾子の組み合わせ
,, .. + と .. ,, + は同じ動作です（順序非依存）

[ 1 ] [ 2 ] [ 3 ] [ 3 ] .. ,, +
# → [ 1 ] [ 2 ] [ 3 ] [ 6 ]
（スタックモードかつ保持モードで加算）

## 注意
- ワード実行後、自動的に消費モード(,)にリセットされます
- 保持モードは演算の連鎖に便利です"#,

        _ => return None,
    };
    Some(result.to_string())
}
